def add (a,b):
    answer = a + b
    return answer

def subtract(num1,num2):
    subtraction = num1-num2
    return subtraction

def divide(num3,num4):
    division = num3 / num4
    return division

def multiply(x,y):
    multiplication = x*y
    return multiplication

def remainder(y,z):
    modulus = y % z
    return modulus